pub fn default_archaic_map() -> Vec<(char, char)> {
    ARCHAIC_JAMO_MAP.to_vec()
}

pub fn apply_old_hangul_rules(rules: &mut RuleSystem, store: &mut GlyphStore, map: &[(char, char)], copy_pixels: bool) {
    for group in &mut rules.groups {
        let kind = group.target;
        let modern_members: Vec<char> = group.members.iter().copied().collect();
        for &(archaic, modern) in map {
            if modern_members.contains(&modern) && !group.members.contains(&archaic) && jamo_matches_kind(archaic, kind) {
                group.members.insert(archaic);
                let key = GlyphKey::new(kind, archaic, &group.id);
                store.ensure_glyph(key.clone());
                if copy_pixels {
                    let modern_key = GlyphKey::new(kind, modern, &group.id);
                    if let Some(src) = store.get(&modern_key).cloned()
                        && let Some(dst) = store.get_mut(&key)
                        && dst.pixels.is_empty()
                    {
                        dst.pixels = src.pixels;
                    }
                }
            }
        }
    }

    for sel in &mut rules.selectors {
        extend_include(&mut sel.cho_set, map);
        extend_include(&mut sel.jung_set, map);
        extend_include(&mut sel.jong_set, map);
    }

    for tpl in &mut rules.templates {
        for vr in &mut tpl.variant_rules {
            extend_include(&mut vr.cho_set, map);
            extend_include(&mut vr.jung_set, map);
            extend_include(&mut vr.jong_set, map);
        }
    }
}

pub fn jamo_matches_kind(c: char, kind: HangulComponent) -> bool {
    let code = c as u32;
    match kind {
        HangulComponent::Cho => (0x1113..=0x115E).contains(&code) || (0xA960..=0xA97C).contains(&code),
        HangulComponent::Jung => (0x1176..=0x11A7).contains(&code) || (0xD7B0..=0xD7C6).contains(&code),
        HangulComponent::Jong => (0x11C3..=0x11FF).contains(&code) || (0xD7CB..=0xD7FB).contains(&code),
    }
}

fn extend_include(cond: &mut CharSetCond, map: &[(char, char)]) {
    if let CharSetCond::Include(set) = cond {
        let modern_chars: Vec<char> = set.iter().copied().collect();
        for modern in modern_chars {
            for &(archaic, mapped) in map {
                if mapped == modern {
                    set.insert(archaic);
                }
            }
        }
    }
}
